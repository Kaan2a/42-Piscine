void	ft_putstr(char *str)
{
	int	n;

	
	*str = 'c';
	*(str+1) = 'c';
	*(str+2) = 'c';

	
	str[0] = 'c';
	str[1] = 'c';
	str[2] = 'c';

		
	n = 0;
	while (*str != '\0')
	{
		write(1, str++, 1);
	}
}
